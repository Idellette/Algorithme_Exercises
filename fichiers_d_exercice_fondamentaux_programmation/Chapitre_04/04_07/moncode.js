// Compléter le code suivant pour créer une boucle qui s’exécute tant que i est inférieur ou égale à 10
let i = 0;
while ( i <= 10 ) {
    document.write(i + " ");
    i++;
}

// Compléter le code suivant pour créer une boucle allant de 0 à 9
for ( let i = 0 ; i < 10  ; i++ ) {
    document.write(i + " ");
}
