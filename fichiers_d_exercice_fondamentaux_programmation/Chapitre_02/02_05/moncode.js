// Chapitre 02_05 _ Travailler avec les opérateurs arithmétiques

// Exemple 1
console.log( 5 + 2 ); // 8
console.log( 5 - 2 ); // 3
console.log( 5 * 2 ); // 10 
console.log( 5 / 2 ); // 2.5
console.log( 5 % 2 ); // 1
console.log( 5 ** 2 ); // 25 (Rappel : 5 exposant 2 équivaut à 5x5)
console.log( 4 + 1 + (5 * 10) ); // 55

// Exemple 2
let calcul = 10
calcul += 2; // équivaut à : calcul = calcul + 2;
console.log(calcul);

// Exemple 3
let calculBis = 10
calculBis++; // équivaut à : calcul = calcul + 1;
console.log(calculBis);

