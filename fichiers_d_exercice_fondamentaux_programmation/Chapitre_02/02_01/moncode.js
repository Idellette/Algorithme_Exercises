// Chapitre 02_01 _ Comprendre ce qu'est une variable et une constante

// Exemple 1 : Créer une variable
let pseudo;
pseudo = "Bob";
alert(pseudo); // Résultat : "Bob"


// Exemple 2 : Modifier la valeur d'une variable
let pseudo2;
pseudo2 = "Bob";
pseudo2 = "Emmanuelle"; 
alert(pseudo2); // Résultat : "Emmanuelle"


// Exemple 3 : Créer une constante
const id = 123;
// id = 456; il n'est pas possible de modifier la valeur d'une constante 
alert(id);